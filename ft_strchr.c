#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i])
	{
		if (s[i] == c)
			return ((char *) &s[i]);
		i++;
	}
	return (NULL);
}

#include <stdio.h>
int	main(void)
{
	printf("%s", ft_strchr("langage", 'a'));
}