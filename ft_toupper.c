#include "libft.h"

int	ft_toupper(int c)
{
	if (c >= 'a' && c <= 'z')
		c -= 32;
	return (c);
}
#include <stdio.h>
int	main(void)
{
	printf("%d", ft_toupper('l'));
	return (0);
}