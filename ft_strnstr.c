#include "libft.h"
#include <string.h>

char	*ft_strnstr(const char *big, const char *little, size_t len)
{
	size_t	i;
	size_t	j;

	if (!little)
		return ((char *) &big[0]);
	i = 0;
	while (big[i] && i < len)
	{
		j = 0;
		while (big[i + j] == little[j] && (i + j) < len)
		{
			if (little[j + 1] == '\0')
				return ((char *) &big[i]);
			j++;
		}
		i++;
	}
	return (0);
}
#include <stdio.h>

int	main(void)
{
	char	big[] = "hello world";
	char	little[] = "hello";
	size_t	len = 5;
	printf("%s", ft_strnstr(big, little, len));
	return (0);
}
