#include "libft.h"

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		i++;
	}
	return (i);
}

#include <stdio.h>
int	main(void)
{
	printf("%d", ft_strlen("elhadj"));
	return (0);
}