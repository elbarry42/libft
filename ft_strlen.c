#include "libft.h"

size_t	ft_strlen(const char *s)
{
	int	i;

	i = 0;
	while (s[i])
		i++;
	return (i);
}

#include <stdio.h>
int	main(void)
{
	printf("%zu", ft_strlen("elhadj"));
	return (0);
}